public class JavaApplication42 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int val=1;
        while (val<=10){
            System.out.println(val + "-");
            val++;
        }
        System.out.println();

        val=1;
        do{
            System.out.println(val + "-");
            val++;
        }while(val<=10);
    }
    
    
}
